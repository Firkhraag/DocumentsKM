CREATE TABLE marks (
	id int identity(1, 1) PRIMARY KEY,
	code varchar(40) NOT NULL,
	name varchar(255) NOT NULL,
	subnode_id smallint NOT NULL,
	department_id smallint NOT NULL,
	signed1_id smallint,
	signed2_id smallint,
	chief_specialist_id smallint,
	group_leader_id smallint,
	main_builder_id smallint,
	issue_date date,
	num_of_volumes smallint,
	edited_date date,
	paintwork_type varchar(4),
	note varchar(255),
	fire_hazard_category_id smallint,
	p_transport boolean,
	p_site boolean,
	UNIQUE (code, subnode_id),
	CONSTRAINT fk_subnode FOREIGN KEY(subnode_id) REFERENCES subnodes(id),
	CONSTRAINT fk_chief_specialist FOREIGN KEY(chief_specialist_id) REFERENCES employees(id),
	CONSTRAINT fk_group_leader FOREIGN KEY(group_leader_id) REFERENCES employees(id),
	CONSTRAINT fk_main_builder FOREIGN KEY(main_builder_id) REFERENCES employees(id),
	CONSTRAINT fk_fire_hazard_category FOREIGN KEY(fire_hazard_category_id) REFERENCES fire_hazard_categories(id)
);